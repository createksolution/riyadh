        $user = User::first();
        // $expire =  $user->expire;
        $expire =  15;
        $Date = date("Y-m-d");
        $form_data = array(
            "proprety" =>   $request->proprety,
            "expire" => date("Y-m-d", strtotime($Date. " + ".$expire." days")),
        );
        Promotion::create($form_data);
        return redirect()->back()->with(['promotion'=>'true']);
